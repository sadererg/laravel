<p><img src="./7.jpg"width="500" height="500"> </p>
                <p>История создания Ангарского политехнического техникума связана с развитием нефтехимии в Иркутской области и строительством в Ангарске нефтехимического комбината.

Техникум организован 1 августа 1947 года в Иркутске на базе авиационного техникума и назывался Иркутским техникумом исскуственного жидкого топлива и газа (ИЖТ).</p>

                
