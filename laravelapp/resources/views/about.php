            <div class="col-lg-4">
                <p><img src="./8.jpg"width="500" height="500"> </p>

                <p>В 1950 году техникум переводится в Ангарск и арендует помещение школы №2 по улице Иркутской, а в 1951 году техникум получает постоянный учебно — лабораторный комплекс.

В 1952 году открывается вечернее отделение и производится набор первой группы.

10 августа 1957 года техникум переименовывается в Ангарский строительный техникум, в него влились Иркутский строительный техникум и Иркутский энергостроительный техникум.</p>

            <div class="col-lg-4">
                <p><img src="./9.jpg"width="500" height="500"> </p>

                <p>В3 октября 1958 года при техникуме создается заочное отделение. В связи с широкой подготовкой специалистов различных отраслей народного хозяйства 1 декабря 1961 года техникум получает статус Ангарского политехнического техникума.

С декабря 1962 года в Братске открыт учебно — консультационный пункт заочного отделения техникума, а с 1972 года образован Хабаровский учебно — консультационный пункт при Хабаровском НПЗ.

Свой первый выпуск Ангарский политехнический техникум осуществил в 1950 году, еще находясь в Иркутске. Путевку в жизнь получили 31 техников-механиков по специальности «Оборудование заводов ИЖТ».</p>

            <div class="col-lg-4">
                <p><img src="./10.jpg"width="500" height="500"> </p>

                <p>За период с 1950 по 1960 годы техникум окончили на дневном отделении около 3000 человек, на вечернем — 398 человек. К 1960 году общий контингент студентов составлял 1619 человек. Занятия в техникуме шли в три смены.

С 1960 по 1970 годы идет большая кропотливая творческая работа педагогического коллектива по развитию и совершенствованию учебно-материальной базы в свете требований научно-технического прогресса.

Так создаются многие лаборатории: «Процессов и аппаратов», «Электрических дисциплин», кабинеты «Технологии металлов» и другие.

С 1992 года начинается обучение по специальности «Экономика и бухгалтерский учет», а с 1993 года объявлен набор на специальность «Техническое обслуживание и ремонт автомобильного транспорта».</p>
